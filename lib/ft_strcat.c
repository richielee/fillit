#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while (*(((unsigned char*)s2) + j))
	{
		s1[i] = *(((unsigned char*)s2) + j);
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
